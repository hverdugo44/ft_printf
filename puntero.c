#include "ft_printf.h"

int	puntero(unsigned long i)
{
	int	x;

	x = 1;
	if (i == 0)
	{
		write(1, "(nil)", 5);
		return (5);
	}
	x += write(1, "0x", 2);
	hexadecimal(i, &x);
	return (x);
}
